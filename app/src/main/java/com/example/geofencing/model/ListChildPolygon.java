package com.example.geofencing.model;

public class ListChildPolygon {
    private String key;
    private String name;

    public ListChildPolygon(String key, String name) {
        this.key = key;
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }
}
